package concepts.jira_api;

public class JiraPerson {
    private String name;
    private String displayName;
    private String key;
    private String emailAddress;

    public String getName() {
        return name;
    }

    public String getDisplayName() {
        return displayName;
    }

    public String getKey() {
        return key;
    }

    public String getEmailAddress() {
        return emailAddress;
    }

}
